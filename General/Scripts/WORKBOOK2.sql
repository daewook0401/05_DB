SELECT
	STUDENT_NAME "학생 이름",
	STUDENT_ADDRESS "주소지"
FROM
	TB_STUDENT
ORDER BY
	STUDENT_NAME;

-- 2번
SELECT
	STUDENT_NAME,
	STUDENT_SSN
FROM
	TB_STUDENT
WHERE ABSENCE_YN='Y'
ORDER BY STUDENT_SSN DESC;

-- 3번
SELECT
	STUDENT_NAME "학생이름",
	STUDENT_NO "학번",
	STUDENT_ADDRESS "거주지 주소"
FROM
	TB_STUDENT
WHERE
	STUDENT_NO LIKE '9%'
AND
	STUDENt_ADDRESS LIKE '경기%'
ORDER BY
	STUDENT_NAME;


-- 4번
SELECT
	PROFESSOR_NAME,
	PROFESSOR_SSN
FROM
	TB_PROFESSOR P
JOIN
	TB_DEPARTMENT D ON (P.DEPARTMENT_NO = D.DEPARTMENT_NO)
WHERE
	D.DEPARTMENT_NAME = '법학과'
ORDER BY PROFESSOR_SSN;


-- 5번
SELECT
	STUDENT_NO,
	TO_CHAR(POINT, 'FM9.00') "학점"
FROM TB_GRADE
WHERE CLASS_NO = 'C3118100'
AND TERM_NO = 200402
ORDER BY POINT DESC;


-- 6번
SELECT
	STUDENT_NO,
	STUDENT_NAME,
	DEPARTMENT_NAME
FROM
	TB_STUDENT S
JOIN
	TB_DEPARTMENT D ON(S.DEPARTMENT_NO = D.DEPARTMENT_NO)
ORDER BY STUDENT_NAME;


